"""
test_cross_stack_fidelity.py — Cross-Stack Theta Vector Fidelity
══════════════════════════════════════════════════════════════════════════════
Verifies that the Python Muse 2 pipeline and the C++ kernel pipeline (via a
pre-computed numpy fixture) produce theta vectors with Pearson correlation
> 0.95 for the same synthetic EEG segment.

Also validates:
  • n_channels=4 and n_channels=64 both produce theta shape (240,)
  • OutputResampler 256→1000 Hz group delay < 2 ms
  • Resampled window length matches expected n_out
  • SpatialEmbedding (PCA) reduces N channels to 12 components

The C++ reference fixture is generated by `scripts/generate_cpp_fixture.py`
(not required to be present — if absent, a synthetic reference is used).
"""

