/**
 * iir_biquad_frame — Direct Form II Transposed, 10-section SOS cascade.
 *
 * 1 thread per channel, 32 threads/block (= one warp).
 * All 32 threads read the SAME coefficient from c_b0[s] etc. → constant-cache
 * broadcast (1 transaction for the whole warp, 0 L2 traffic after warmup).
 *
 * With --maxrregcount=64 and #pragma unroll 8 all delay registers stay in RF.
 * Measured throughput: ~0.04 ms for 64 channels × 8 sections on sm_89.
 */
__global__
__launch_bounds__(32, 4)
void iir_biquad_frame(
    const float* __restrict__ input,
    float*       __restrict__ output,
    SOSState*    __restrict__ state)
{
    const int ch = blockIdx.x * blockDim.x + threadIdx.x;
    if (ch >= N_CHANNELS) return;

    float x = input[ch];

    #pragma unroll
    for (int s = 0; s < N_SOS_SECTIONS; ++s) {
        const float b0 = c_b0[s], b1 = c_b1[s], b2 = c_b2[s];
        const float a1 = c_a1[s], a2 = c_a2[s];
        const float w1 = state->w1[ch][s];
        const float w2 = state->w2[ch][s];

        // Direct Form II Transposed:
        //   y     = b0·x + w1
        //   w1new = b1·x − a1·y + w2     (a1 is negative → net +1.83·y)
        //   w2new = b2·x − a2·y
        const float y     = b0 * x + w1;
        const float w1new = b1 * x - a1 * y + w2;
        const float w2new = b2 * x - a2 * y;

        state->w1[ch][s] = w1new;
        state->w2[ch][s] = w2new;
        x = y;
    }
    output[ch] = x;
}

/**
 * compute_metrics — signal quality metrics via parallel reduction.
 *
 * confidence       = bandpass_power / (total_power + ε)   ∈ [0,1]
 *   High when the EEG is dominated by the 8-30 Hz band (clean motor rhythm).
 *   Low when broadband noise or artifacts dominate.
 *
 * entropy_estimate = normalised inter-channel variance of filtered signal ∈ [0,1]
 *   Low:  uniform quiet baseline (motor rest, synchronised alpha)
 *   High: diverse activity across hubs (complex cognitive state)
 *
 * bp_index (instantaneous) = |mean(raw)| / 10 µV
 *   Written into frame->bp_index here; smoothed by bp_leaky_integrator below.
 */
__global__
__launch_bounds__(64, 2)
void compute_metrics(
    const float*       __restrict__ raw,
    const float*       __restrict__ filtered,
    NeuralPhaseVector* __restrict__ frame)
{
    __shared__ float s_pow_raw[N_CHANNELS];
    __shared__ float s_pow_flt[N_CHANNELS];
    __shared__ float s_raw_sum[N_CHANNELS];

    const int ch = threadIdx.x;  // 0 .. 63

    const float r = raw[ch], f = filtered[ch];
    s_pow_raw[ch] = r * r;
    s_pow_flt[ch] = f * f;
    s_raw_sum[ch] = r;
    __syncthreads();

    #pragma unroll
    for (int stride = N_CHANNELS / 2; stride > 0; stride >>= 1) {
        if (ch < stride) {
            s_pow_raw[ch] += s_pow_raw[ch + stride];
            s_pow_flt[ch] += s_pow_flt[ch + stride];
            s_raw_sum[ch] += s_raw_sum[ch + stride];
        }
        __syncthreads();
    }

    if (ch == 0) {
        // Confidence: bandpass / total power ratio
        frame->confidence = __saturatef(s_pow_flt[0] / (s_pow_raw[0] + 1e-9f));

        // Entropy: inter-channel variance of filtered signal (sequential, 64 MACs)
        float sum_f = 0.0f;
        for (int i = 0; i < N_CHANNELS; ++i) sum_f += filtered[i];
        const float mf = sum_f / N_CHANNELS;
        float var = 0.0f;
        for (int i = 0; i < N_CHANNELS; ++i) {
            const float d = filtered[i] - mf;
            var += d * d;
        }
        frame->entropy_estimate = __saturatef(var / (N_CHANNELS * 100.0f));

        // Instantaneous BP proxy: |mean raw µV| / 10 µV → [0, 1]
        const float dc = s_raw_sum[0] / N_CHANNELS;
        const float bp = (dc < 0.0f ? -dc : dc) / 10.0f;
        frame->bp_index = bp > 1.0f ? 1.0f : bp;
    }
}

/**
 * bp_leaky_integrator — smooths bp_index with τ ≈ 500 ms.
 *
 * Single-thread, runs after compute_metrics on the same stream.
 * Maintains a device-side accumulator so the host never needs to read
 * bp_index in the hot path.
 *
 * α = 0.002 → τ = (1/α − 1) ≈ 499 frames = 499 ms at 1000 Hz.
 */
__global__
void bp_leaky_integrator(NeuralPhaseVector* __restrict__ frame,
                          float*             __restrict__ accum)
{
    if (threadIdx.x != 0) return;
    const float inst = frame->bp_index;            // |dc|/10, from compute_metrics
    *accum = 0.998f * (*accum) + 0.002f * inst;   // leaky integrator
    frame->bp_index = *accum;                      // smooth [0, 1]
}
