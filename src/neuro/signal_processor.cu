/**
 * signal_processor.cu  —  KHAOS Real-Time EEG Signal Processor
 *
 * Pipeline:
 *   LSL → pinned ring buffer → iir_biquad_frame (8-section SOS)
 *       → d_filt_ → DWTExtractor::process_frame() (dwt.cu, same stream)
 *                 → compute_metrics → NeuralPhaseVector.conf / entropy / bp
 *
 * SOS filter:  8th-order Butterworth bandpass 8–30 Hz, fs = 1000 Hz.
 *              scipy.signal.iirfilter(8,[8,30],'bandpass','butter',fs=1000)
 *              → zpk2sos → 8 second-order sections.
 *              Coefficients in __constant__ memory (160 bytes, fits constant cache).
 *
 * Dispatch:    CUDA Graph captures IIR + metrics.  Launch overhead ≈ 1 µs.
 *              DWT kernels (dwt.cu) launched on the same stream after the graph.
 *
 * sm_89 tuning (RTX 5060 Ti):
 *   __launch_bounds__(32,4) on IIR kernel  → ≥4 blocks/SM, ~50% occupancy
 *   __launch_bounds__(64,2) on metrics     → 2 blocks/SM, shared-mem bound
 *   --maxrregcount=64 (CMakeLists.txt)     → no RF spills, max occupancy
 *   #pragma unroll on 8-section loop       → all SOS ops in registers
 *
 * Build:  -DETHICS_COMPLIANT enforced by CMakeLists.txt
 */

